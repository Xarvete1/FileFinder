#!/bin/sh

# Проверяем, передан ли аргумент
if [ "$#" -ne 1 ]; then
    echo "Использование: $0 <имя_файла>"
    exit 1  # Завершает выполнение со статусом ошибки
fi

# Получаем имя файла из аргументов
FILENAME="$1"

# Выполняем поиск файлов с помощью команды find
echo "Поиск файлов с именем: $FILENAME"
RESULT=$(find / -name "$FILENAME" 2>/dev/null)

# Проверяем, нашлись ли файлы
if [ -z "$RESULT" ]; then
    echo "Файлы с именем '$FILENAME' не найдены."
    exit 0  # Завершает выполнение без ошибок
else
    echo "Найденные файлы:"
    echo "$RESULT"
    exit 0  # Завершает выполнение без ошибок
fi
